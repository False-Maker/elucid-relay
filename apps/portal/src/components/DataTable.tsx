import type { ReactNode } from "react";

export function Metric({ label, value, detail }: { label: string; value: string; detail?: string }) {
  return <section className="metric"><span>{label}</span><strong>{value}</strong>{detail && <small>{detail}</small>}</section>;
}

export function DataTable({ rows, columns, action }: { rows: any[]; columns: string[]; action?: (row: any) => ReactNode }) {
  if (!rows.length) return <div className="empty">暂无记录</div>;
  return (
    <div className="table-wrap">
      <table>
        <thead><tr>{columns.map((column) => <th key={column}>{columnLabel(column)}</th>)}{action && <th className="table-actions-head">操作</th>}</tr></thead>
        <tbody>
          {rows.map((row, index) => (
            <tr key={row.id ?? index}>{columns.map((column) => <td key={column}>{cell(row[column])}</td>)}{action && <td className="table-actions-cell">{action(row)}</td>}</tr>
          ))}
        </tbody>
      </table>
    </div>
  );
}

