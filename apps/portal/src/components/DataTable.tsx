function columnLabel(column: string) {
  return columnLabels[column] ?? column;
}

function cell(value: unknown) {
  if (Array.isArray(value)) {
    if (!value.length) return "";
    if (value.some((item) => item && typeof item === "object")) {
      return <code className="cell-json">{JSON.stringify(value)}</code>;
    }
    return <span className="cell-list">{value.map((item) => String(item)).join(", ")}</span>;
  }
  if (value && typeof value === "object") return <code className="cell-json">{JSON.stringify(value)}</code>;
  if (typeof value === "boolean") return value ? "是" : "否";
  const text = String(value ?? "");
  if (isStatus(text)) return <StatusBadge status={text} />;
  if (text in valueLabels) return valueLabels[text];
  if (isMonoValue(text)) return <code>{text}</code>;
  return text;
}

