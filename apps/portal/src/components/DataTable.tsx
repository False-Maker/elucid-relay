function isMonoValue(value: string) {
  return /^(req_|rk_|sk-|[a-f0-9-]{12,}|https?:\/\/|\d{4}-\d{2}-\d{2}|[\w.-]+\/[\w.-]+$)/i.test(value);
}

function StatusBadge({ status }: { status: string }) {
  const normalized = status.replace(/_/g, "-").toLowerCase();
  return (
    <span className={`status-badge ${normalized}`}>
      <span />
      {statusLabel(status)}
    </span>
  );
}

